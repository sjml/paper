                cmd="paper__save"
                ;;
            paper,watch)
                cmd="paper__watch"
                ;;
            paper,wc)
                cmd="paper__wc"
                ;;
            paper,web)
                cmd="paper__web"
                ;;
            paper__help,build)
                cmd="paper__help__build"
                ;;
            paper__help,dev)
                cmd="paper__help__dev"
                ;;
            paper__help,fmt)
                cmd="paper__help__fmt"
                ;;
            paper__help,help)
                cmd="paper__help__help"
                ;;
