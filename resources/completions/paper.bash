                cmd="paper__help__new"
                ;;
            paper__help,push)
                cmd="paper__help__push"
                ;;
            paper__help,save)
                cmd="paper__help__save"
                ;;
            paper__help,watch)
                cmd="paper__help__watch"
                ;;
            paper__help,wc)
                cmd="paper__help__wc"
                ;;
            paper__help,web)
                cmd="paper__help__web"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        paper)
            opts="-v -h --version --verbose --help new init dev build save push web wc watch fmt help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        paper__build)
            opts="-t -v -h --output-format --docx-revision --verbose --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --output-format)
                    COMPREPLY=($(compgen -W "docx latex latex+pdf json" -- "${cur}"))
                    return 0
                    ;;
                -t)
                    COMPREPLY=($(compgen -W "docx latex latex+pdf json" -- "${cur}"))
                    return 0
                    ;;
                --docx-revision)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        paper__dev)
            opts="-v -h --verbose --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
