_paper() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="paper"
                ;;
            paper,build)
                cmd="paper__build"
                ;;
            paper,dev)
                cmd="paper__dev"
                ;;
            paper,fmt)
                cmd="paper__fmt"
                ;;
            paper,help)
